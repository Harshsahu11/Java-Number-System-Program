public class Conditional2 {
    public static void main(String[] args) {
        int n1=10;
        int n2=20;
        if(n1==n2)
        {
            System.out.println("Both are Equals");
        }
        else if (n1>n2)
        {
            System.out.println("n1 is Greater than n2");
        }
        else {
            System.out.println("n2 is Greater than n1");
        }
    }
}
